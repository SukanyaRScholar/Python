/* the following creates the tables */

/* create rating table */
CREATE TABLE Rating (
  rating_ID integer not null,
  rating_AGE integer,
  rating_DESC char(200),
  PRIMARY KEY (rating_ID)
);
/* create plan table */
CREATE TABLE Plan (
  plan_ID integer not null,
  plan_Descript varchar(200),
  plan_Name varchar(200) NOT NULL constraint chkplan check (plan_Name in('BASIC' ,'STANDARD', 'PREMIUM')), /* plan has to be one of the three */
  plan_Amount number,
  PRIMARY KEY (plan_ID)
);

/* create genre table */
CREATE TABLE Genre (
  genre_ID integer not null,
  genre_Name char(50),
  genre_Description char(70),
  PRIMARY KEY (genre_ID)
);

/* create account table */
CREATE TABLE Account (
  account_ID integer not null,
  account_userName varchar(200) not null,
  account_PW varchar(200) not null,
  account_Fname varchar(200) not null,
  account_Mname varchar(200),
  account_Lname varchar(200) not null,
  account_creditcardNo number not null,
  plan_ID integer not null,
  PRIMARY KEY (account_ID),
  FOREIGN KEY (plan_ID) REFERENCES plan
);
/* create viewer table */
CREATE TABLE Viewer (
  viewer_ID integer not null,
  viewer_profileName varchar(200) not null,
  account_ID integer not null,
  rating_ID integer,
  PRIMARY KEY (viewer_ID),
FOREIGN KEY (account_ID) references account,
FOREIGN KEY (rating_ID) references rating
);



/* create streaming table */

CREATE TABLE Streaming (
  stream_ID integer not null,
  stream_Genre char(200),
  plan_ID integer,
  rating_ID integer,
  stream_Watchhistory varchar(200),
  genre_ID integer,
  PRIMARY KEY (stream_ID),
FOREIGN KEY (plan_ID) references plan,
FOREIGN KEY (rating_ID) references rating,
FOREIGN KEY (genre_ID) references genre
);


/* create recommendations table */
CREATE TABLE Recommendations (
  recomm_ID integer not null,
  recomm_ProgName varchar(200),
  genre_ID integer,
  viewer_ID integer,
  PRIMARY KEY (recomm_ID),
  FOREIGN KEY (genre_ID) references genre,
  FOREIGN KEY (viewer_ID) references viewer

);


